import React from 'react';
interface CardProps {
  children: React.ReactNode;
  className?: string;
  hover?: boolean;
}
export function Card({
  children,
  className = '',
  hover = false
}: CardProps) {
  return <div className={`
        bg-white border border-gray-100 rounded-lg overflow-hidden
        ${hover ? 'transition-all duration-300 hover:shadow-xl hover:-translate-y-1 hover:border-red-100' : 'shadow-sm'}
        ${className}
      `}>
      {children}
    </div>;
}